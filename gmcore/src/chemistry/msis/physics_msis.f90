    real(dp) function dnet(dd,dm,zhm,xmm,xm)
      implicit none
!
!     DD - diffusive density
!     DM - full mixed density
!     ZHM - transition scale length
!     XMM - full mixed molecular weight
!     XM  - species molecular weight
!     DNET - combined density
!
      real(dp) , intent(in) :: dm , xm , xmm , zhm
      real(dp) , intent(inout) :: dd
!
      real(dp) :: a , ylog
!
      a = zhm/(xmm-xm)
      if ( dm <= d_0 .or. dd <= d_0 ) then
        if ( dabs(dd) < nearzero .and. dabs(dm) < nearzero ) dd = d_1
        if ( dabs(dm) < nearzero ) then
          dnet = dd
        else if ( dabs(dd) < nearzero ) then
          dnet = dm
        end if
      else
        ylog = a*dlog(dm/dd)
        if ( ylog < -d_10 ) then
          dnet = dd
        else if ( ylog > d_10 ) then
          dnet = dm
        else
          dnet = dd*(d_1+dexp(ylog))**(d_1/a)
        end if
      end if
    end function dnet
