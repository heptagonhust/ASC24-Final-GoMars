!   To get current values of SW: CALL TRETRV(SW)
!
    subroutine tselec(sv)
      implicit none
!
      real(dp) , dimension(25) , intent(in) :: sv
!
      integer :: i

      do i = 1 , 25
        sav(i) = sv(i)
        sw(i) = dmod(sv(i),d_2)
        if ( dabs(sv(i)-d_1) < nearzero .or. &
             dabs(sv(i)-d_2) < nearzero ) then
          swc(i) = d_1
        else
          swc(i) = d_0
        end if
      end do
      isw = 64999
      return
    end subroutine tselec
