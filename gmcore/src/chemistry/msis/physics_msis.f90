!
!     FIND ALTITUDE OF PRESSURE SURFACE (PRESS) FROM GTD7 INPUT:
!     IYD - YEAR AND DAY AS YYDDD
!     SEC - UT(SEC)
!     GLAT - GEODETIC LATITUDE(DEG)
!     GLONG - GEODETIC LONGITUDE(DEG)
!     STL - LOCAL APPARENT SOLAR TIME(HRS)
!     F107A - 3 MONTH AVERAGE OF F10.7 FLUX
!     F107 - DAILY F10.7 FLUX FOR PREVIOUS DAY
!     AP - MAGNETIC INDEX(DAILY) OR WHEN SW(9)=-1. :
!     - ARRAY CONTAINING:
!     (1) DAILY AP
!     (2) 3 HR AP INDEX FOR CURRENT TIME
!     (3) 3 HR AP INDEX FOR 3 HRS BEFORE CURRENT TIME
!     (4) 3 HR AP INDEX FOR 6 HRS BEFORE CURRENT TIME
!     (5) 3 HR AP INDEX FOR 9 HRS BEFORE CURRENT TIME
!     (6) AVERAGE OF EIGHT 3 HR AP INDICIES FROM 12 TO 33 HRS PRIOR
!     TO CURRENT TIME
!     (7) AVERAGE OF EIGHT 3 HR AP INDICIES FROM 36 TO 59 HRS PRIOR
!     TO CURRENT TIME
!     PRESS - PRESSURE LEVEL(MB)
!     OUTPUT:
!     ALT - ALTITUDE(KM)
!     D(1) - HE NUMBER DENSITY(CM-3)
!     D(2) - O NUMBER DENSITY(CM-3)
!     D(3) - N2 NUMBER DENSITY(CM-3)
!     D(4) - O2 NUMBER DENSITY(CM-3)
!     D(5) - AR NUMBER DENSITY(CM-3)
!     D(6) - TOTAL MASS DENSITY(GM/CM3)
!     D(7) - H NUMBER DENSITY(CM-3)
!     D(8) - N NUMBER DENSITY(CM-3)
!     D(9) - HOT O NUMBER DENSITY(CM-3)
!     T(1) - EXOSPHERIC TEMPERATURE
!     T(2) - TEMPERATURE AT ALT
!
    subroutine ghp7(iyd,sec,alt,glat,glong,stl,f107a,f107,ap,d,t,press)
      implicit none
!
      real(dp) , intent(in) :: f107 , f107a , glat , glong , press , sec , stl
      real(dp) , intent(out) :: alt
      integer , intent(in) :: iyd
      real(dp) , intent(in) , dimension(7) :: ap
      real(dp) , intent(inout) , dimension(9) :: d
      real(dp) , intent(inout) , dimension(2) :: t
!
      real(dp) :: ca , cd , cl , cl2 , diff , g , p , pl , sh , &
                 xm , xn , z , zi
      integer :: iday , l

      real(dp) , parameter :: bm = 1.3806E-19_r8
      real(dp) , parameter :: test = 0.00043_r8
      integer , parameter :: ltest = 12
!
      pl = dlog10(press)
!
!     Initial altitude estimate
      if ( pl >= -5.0_r8 ) then
        if ( pl > 2.5_r8 ) zi = 18.06_r8*(3.00_r8-pl)
        if ( pl > 0.75_r8 .and. pl <= 2.5_r8 ) zi = 14.98_r8*(3.08_r8-pl)
        if ( pl > -1.0_r8 .and. pl <= 0.75_r8 ) zi = 17.8_r8*(2.72_r8-pl)
        if ( pl > -2.0_r8 .and. pl <= -1.0_r8 ) zi = 14.28_r8*(3.64_r8-pl)
        if ( pl > -4.0_r8 .and. pl <= -2.0_r8 ) zi = 12.72_r8*(4.32_r8-pl)
        if ( pl <= -4.0_r8 ) zi = 25.3*(.11-pl)
        iday = mod(iyd,1000)
        cl = glat / d_090
        cl2 = cl*cl
        if ( iday < 182 ) cd = d_1 - iday/91.25_r8
        if ( iday >= 182 ) cd = iday / 91.25_r8 - 3.0_r8
        ca = d_0
        if ( pl > -1.11_r8 .and. pl <= -0.23_r8 ) ca = d_1
        if ( pl > -0.23_r8 ) ca = (2.79_r8-pl)/(2.79_r8+0.23_r8)
        if ( pl <= -1.11_r8 .and. pl > -3.0_r8 ) &
          ca = (-2.93_r8-pl)/(-2.93_r8+1.11_r8)
        z = zi - 4.87_r8*cl*cd*ca - 1.64_r8*cl2*ca + 0.31_r8*ca*cl
      end if
      if ( pl < -5.0_r8 ) z = 22.0_r8*(pl+4.0_r8)**d_2 + 110.0_r8
      ! ITERATION LOOP
      l = 0
      do
        l = l + 1
        call gtd7(iyd,sec,z,glat,glong,stl,f107a,f107,ap,48,d,t)
        xn = d(1) + d(2) + d(3) + d(4) + d(5) + d(7) + d(8)
        p = bm*xn*t(2)
        if ( imr == 1 ) p = p*1.E-6_r8
        diff = pl - dlog10(p)
        if ( abs(diff) < test .or. l == ltest ) then
          ! Non converging , should not happen
          alt = z
          exit
        else
          xm = d(6)/xn/1.66E-24_r8
          if ( imr == 1 ) xm = xm*1.E3_r8
          g = gsurf / (1.0_r8+z/re)**d_2
          sh = r100gas*t(2)/(xm*g)
          ! New altitude estimate using scale height
          if ( l < 6 ) then
            z = z - sh*diff*2.302_r8
          else
            z = z - sh*diff
          end if
          cycle
        end if
      end do
    end subroutine ghp7
