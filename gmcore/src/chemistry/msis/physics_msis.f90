               g0(ap(5))*ex**d_3+(g0(ap(6))*ex**d_4 +    &
               g0(ap(7))*ex**12.0_r8)*(d_1-ex**d_8)/(d_1-ex)))/sumex(ex)
      end function sg0
    end function globe7
