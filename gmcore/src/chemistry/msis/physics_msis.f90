    real(dp) function ccor(alt,r,h1,zh)
      implicit none
      ! alt - Altitude
      ! r   - Target ratio
      ! h1  - Transition scale length
      ! zh  - Altitude of 1/2 r
      real(dp) , intent(in) :: alt , h1 , r , zh
      real(dp) , parameter :: echeck = 70.0_r8
      real(dp) :: e , ex
      e = (alt-zh)/h1
      if ( e > echeck ) then
        ccor = d_0
      else if ( e < -echeck ) then
        ccor = r
      else
        ex = dexp(e)
        ccor = r/(d_1+ex)
      end if
      ccor = dexp(ccor)
    end function ccor
